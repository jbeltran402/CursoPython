# min y max cumplen la funcion de ordenar, ya se numeros, texto o diccionarios

menor = min(1,2,5,6,3,67,8)
print(menor)
mayor = max(1,2,5,6,3,67,8)
print(mayor)

# presenta prioridad por las letras que estan en mayuscula
lista_nombre = ["juan","David","beltran","Garnica"]
print(min(lista_nombre))

dic = {'a1':10,'a2':5,'a3':30}
print(max(dic))
